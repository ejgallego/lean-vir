import { mkdir, readFile, writeFile } from "node:fs/promises";
import { dirname } from "node:path";
import { fileURLToPath } from "node:url";

import { IR_CODEC_TAG_GROUPS } from "./ir-codec-tags.mjs";

const writeMode = process.argv.includes("--write");

const generatedFiles = [
  {
    path: new URL("../Vir/GeneratePackage/PackageIRTags.lean", import.meta.url),
    contents: generateLeanTags(),
  },
  {
    path: new URL("../wasm/upstream_shim/package/package_ir_tags.h", import.meta.url),
    contents: generateCppTags(),
  },
];

function leanName(prefix, cppTag) {
  return `${prefix}${cppTag}`;
}

function headerLines(target) {
  return [
    target === "lean" ? "/-" : "/*",
    "Generated by `node scripts/check-ir-codec-tags.mjs --write`.",
    "Source of truth: `scripts/ir-codec-tags.mjs`.",
    target === "lean" ? "-/" : "*/",
    "",
  ];
}

function validateGroups() {
  const enumNames = new Set();
  const leanNames = new Set();
  for (const group of IR_CODEC_TAG_GROUPS) {
    if (enumNames.has(group.cppEnum)) {
      throw new Error(`duplicate C++ enum name ${group.cppEnum}`);
    }
    enumNames.add(group.cppEnum);

    const values = new Set();
    for (const [leanTag, cppTag, value] of group.tags) {
      const name = leanName(group.leanPrefix, cppTag);
      if (leanNames.has(name)) {
        throw new Error(`duplicate Lean tag constant ${name}`);
      }
      leanNames.add(name);
      if (values.has(value)) {
        throw new Error(`duplicate ${group.cppEnum} value ${value}`);
      }
      values.add(value);
      if (!Number.isInteger(value) || value < 0 || value > 255) {
        throw new Error(`${group.cppEnum}.${leanTag} must be a u8 tag, got ${value}`);
      }
    }
  }
}

function generateLeanTags() {
  validateGroups();
  const lines = [
    ...headerLines("lean"),
    "namespace Vir.GeneratePackage",
    "",
    "namespace PackageIRTags",
    "",
  ];
  for (const group of IR_CODEC_TAG_GROUPS) {
    for (const [_leanTag, cppTag, value] of group.tags) {
      lines.push(`def ${leanName(group.leanPrefix, cppTag)} : Nat := ${value}`);
    }
    lines.push("");
  }
  lines.push("end PackageIRTags", "", "end Vir.GeneratePackage", "");
  return lines.join("\n");
}

function generateCppTags() {
  validateGroups();
  const lines = [
    ...headerLines("cpp"),
    "#pragma once",
    "",
    "#include <stdint.h>",
    "",
    "namespace lean::vir::package_ir_tags {",
    "",
  ];
  for (const group of IR_CODEC_TAG_GROUPS) {
    lines.push(`enum class ${group.cppEnum} : uint8_t {`);
    for (const [leanTag, cppTag, value] of group.tags) {
      lines.push(`    ${cppTag} = ${value},`);
    }
    lines.push("};", "");
  }
  lines.push("} // namespace lean::vir::package_ir_tags", "");
  return lines.join("\n");
}

async function readExisting(path) {
  try {
    return await readFile(path, "utf8");
  } catch (err) {
    if (err && err.code === "ENOENT") {
      return null;
    }
    throw err;
  }
}

for (const file of generatedFiles) {
  const existing = await readExisting(file.path);
  if (writeMode) {
    await mkdir(dirname(fileURLToPath(file.path)), { recursive: true });
    await writeFile(file.path, file.contents);
  } else if (existing !== file.contents) {
    throw new Error(`${fileURLToPath(file.path)} is stale; run npm run generate:ir-codec-tags`);
  }
}

console.log(`IR codec tag files ${writeMode ? "updated" : "are current"}`);
