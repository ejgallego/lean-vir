import { mkdir, readFile, writeFile } from "node:fs/promises";
import { dirname } from "node:path";
import { fileURLToPath } from "node:url";

import { IR_CODEC_TAG_GROUPS } from "./ir-codec-tags.mjs";

const args = process.argv.slice(2);
for (const arg of args) {
  if (arg !== "--write") {
    throw new Error(`unknown argument ${JSON.stringify(arg)}; expected --write or no arguments`);
  }
}
const writeMode = args.includes("--write");

validateGroups();
await validateTagUses();

const generatedFiles = [
  {
    path: new URL("../Vir/GeneratePackage/PackageIRTags.lean", import.meta.url),
    contents: generateLeanTags(),
  },
  {
    path: new URL("../wasm/upstream_shim/package/package_ir_tags.h", import.meta.url),
    contents: generateCppTags(),
  },
];

function leanName(prefix, cppTag) {
  return `${prefix}${cppTag}`;
}

function headerLines(target) {
  return [
    target === "lean" ? "/-" : "/*",
    "Generated by `node scripts/check-ir-codec-tags.mjs --write`.",
    "Source of truth: `scripts/ir-codec-tags.mjs`.",
    target === "lean" ? "-/" : "*/",
    "",
  ];
}

function validateGroups() {
  const enumNames = new Set();
  const leanNames = new Set();
  for (const group of IR_CODEC_TAG_GROUPS) {
    if (enumNames.has(group.cppEnum)) {
      throw new Error(`duplicate C++ enum name ${group.cppEnum}`);
    }
    enumNames.add(group.cppEnum);

    const values = new Set();
    const cppTags = new Set();
    for (const [cppTag, value] of group.tags) {
      if (cppTags.has(cppTag)) {
        throw new Error(`duplicate ${group.cppEnum} tag name ${cppTag}`);
      }
      cppTags.add(cppTag);
      const name = leanName(group.leanPrefix, cppTag);
      if (leanNames.has(name)) {
        throw new Error(`duplicate Lean tag constant ${name}`);
      }
      leanNames.add(name);
      if (values.has(value)) {
        throw new Error(`duplicate ${group.cppEnum} value ${value}`);
      }
      values.add(value);
      if (!Number.isInteger(value) || value < 0 || value > 255) {
        throw new Error(`${group.cppEnum}.${cppTag} must be a u8 tag, got ${value}`);
      }
    }
    for (const [value, reason] of group.reserved ?? []) {
      if (values.has(value)) {
        throw new Error(`${group.cppEnum} reserved tag duplicates value ${value}`);
      }
      values.add(value);
      if (!Number.isInteger(value) || value < 0 || value > 255) {
        throw new Error(`${group.cppEnum} reserved tag must be a u8, got ${value}`);
      }
      if (typeof reason !== "string" || reason.length === 0) {
        throw new Error(`${group.cppEnum} reserved tag ${value} must explain why it is reserved`);
      }
    }
  }
}

async function validateTagUses() {
  const leanPath = new URL("../Vir/GeneratePackage/Emit.lean", import.meta.url);
  const cppPath = new URL("../wasm/upstream_shim/package/package_ir_decoder.cpp", import.meta.url);
  const [leanSource, cppSource] = await Promise.all([
    readFile(leanPath, "utf8"),
    readFile(cppPath, "utf8"),
  ]);
  for (const group of IR_CODEC_TAG_GROUPS) {
    for (const [cppTag] of group.tags) {
      const leanUse = `PackageIRTags.${leanName(group.leanPrefix, cppTag)}`;
      if (!leanSource.includes(leanUse)) {
        throw new Error(`${fileURLToPath(leanPath)} does not use generated tag ${leanUse}`);
      }
      const cppUse = `tags::${group.cppEnum}::${cppTag}`;
      if (!cppSource.includes(cppUse)) {
        throw new Error(`${fileURLToPath(cppPath)} does not use generated tag ${cppUse}`);
      }
    }
  }
}

function generateLeanTags() {
  const lines = [
    ...headerLines("lean"),
    "namespace Vir.GeneratePackage",
    "",
    "namespace PackageIRTags",
    "",
  ];
  for (const group of IR_CODEC_TAG_GROUPS) {
    for (const [cppTag, value] of group.tags) {
      lines.push(`def ${leanName(group.leanPrefix, cppTag)} : Nat := ${value}`);
    }
    for (const [value, reason] of group.reserved ?? []) {
      lines.push(`-- Tag ${value} is reserved: ${reason}.`);
    }
    lines.push("");
  }
  lines.push("end PackageIRTags", "", "end Vir.GeneratePackage", "");
  return lines.join("\n");
}

function generateCppTags() {
  const lines = [
    ...headerLines("cpp"),
    "#pragma once",
    "",
    "#include <stdint.h>",
    "",
    "namespace lean::vir::package_ir_tags {",
    "",
  ];
  for (const group of IR_CODEC_TAG_GROUPS) {
    lines.push(`enum class ${group.cppEnum} : uint8_t {`);
    for (const [cppTag, value] of group.tags) {
      lines.push(`    ${cppTag} = ${value},`);
    }
    for (const [value, reason] of group.reserved ?? []) {
      lines.push(`    // Tag ${value} is reserved: ${reason}.`);
    }
    lines.push("};", "");
  }
  lines.push("} // namespace lean::vir::package_ir_tags", "");
  return lines.join("\n");
}

async function readExisting(path) {
  try {
    return await readFile(path, "utf8");
  } catch (err) {
    if (err && err.code === "ENOENT") {
      return null;
    }
    throw err;
  }
}

for (const file of generatedFiles) {
  const existing = await readExisting(file.path);
  if (writeMode) {
    await mkdir(dirname(fileURLToPath(file.path)), { recursive: true });
    await writeFile(file.path, file.contents);
  } else if (existing !== file.contents) {
    throw new Error(`${fileURLToPath(file.path)} is stale; run npm run generate:ir-codec-tags`);
  }
}

console.log(`IR codec tag files and uses ${writeMode ? "updated" : "are current"}`);
