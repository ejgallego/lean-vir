import { readFile, writeFile } from "node:fs/promises";

import { parseNativeExterns } from "./native-externs.mjs";
import { generatedWrapperNames } from "./native-wrapper-macros.mjs";

const nativeExternsPath = new URL("../Vir/GeneratePackage/NativeExterns.lean", import.meta.url);
const nativeSymbolsPath = new URL("../wasm/upstream_shim/runtime/native_symbols.cpp", import.meta.url);
const nativeRegistryPath = new URL("../wasm/upstream_shim/runtime/native_symbols_registry.inc", import.meta.url);
const writeMode = process.argv.includes("--write");

function cString(value) {
  return JSON.stringify(value);
}

function registryMacro(entry) {
  const isConst = entry.params.length === 0 && entry.symbol.startsWith("l_");
  return isConst
    ? `X_CONST(${cString(entry.name)}, ${cString(entry.symbol)}, &${entry.symbol})`
    : `X(${cString(entry.name)}, ${cString(entry.symbol)}, ${entry.symbol}___boxed)`;
}

function generateNativeRegistry(entries) {
  const shimEntries = entries.filter((entry) => !entry.generateBoxedWrapper);
  return [
    "/*",
    "Generated by `node scripts/check-boundary-registry.mjs --write`.",
    "Source of truth: `Vir/GeneratePackage/NativeExterns.lean` `nativeExterns`.",
    "Shim-owned boxed wrappers live in `runtime/native_symbols.cpp`.",
    "Compiler-generated wrappers are registered by a build-local generated include.",
    "*/",
    "",
    "#define VIR_NATIVE_SYMBOLS(X, X_CONST) \\",
    ...shimEntries.map((entry, index) => {
      const suffix = index + 1 === shimEntries.length ? "" : " \\";
      return `    ${registryMacro(entry)}${suffix}`;
    }),
    "",
  ].join("\n");
}

function parseNativeRegistry(source) {
  const entries = new Map();
  const startMarker = "#define VIR_NATIVE_SYMBOLS(X, X_CONST) \\\n";
  const start = source.indexOf(startMarker);
  if (start < 0) {
    throw new Error("could not find VIR_NATIVE_SYMBOLS table");
  }

  const table = source.slice(start + startMarker.length);
  const entryRegex = /^\s*(X|X_CONST)\("([^"]+)",\s*"([^"]+)",\s*([A-Za-z0-9_&]+)\)\s*\\?$/gm;
  for (const match of table.matchAll(entryRegex)) {
    const kind = match[1];
    const name = match[2];
    const symbol = match[3];
    entries.set(name, {
      symbol,
      dlsymSymbol: kind === "X_CONST" ? symbol : `${symbol}___boxed`,
    });
  }

  return entries;
}

function parseBoxedWrappers(source) {
  const wrappers = new Set(
    [...source.matchAll(/extern "C" lean_object \* ([A-Za-z0-9_]+___boxed)\(/g)].map((match) => match[1])
  );
  for (const wrapper of generatedWrapperNames(source)) {
    wrappers.add(wrapper);
  }
  return wrappers;
}

function expectedDlsymSymbol(entry) {
  if (entry.params.length === 0 && entry.symbol.startsWith("l_")) {
    return entry.symbol;
  }
  return `${entry.symbol}___boxed`;
}

function expectedWrapper(entry) {
  const symbol = expectedDlsymSymbol(entry);
  return symbol.endsWith("___boxed") ? symbol : null;
}

const nativeExternsSource = await readFile(nativeExternsPath, "utf8");
const nativeSymbols = await readFile(nativeSymbolsPath, "utf8");

const nativeExterns = parseNativeExterns(nativeExternsSource);
const generatedRegistry = generateNativeRegistry(nativeExterns);
if (writeMode) {
  await writeFile(nativeRegistryPath, generatedRegistry);
}

const nativeRegistry = writeMode ? generatedRegistry : await readFile(nativeRegistryPath, "utf8");
const nativeRegistryEntries = parseNativeRegistry(nativeRegistry);
const boxedWrappers = parseBoxedWrappers(nativeSymbols);
const failures = [];

if (nativeRegistry !== generatedRegistry) {
  failures.push("runtime/native_symbols_registry.inc is stale; run `node scripts/check-boundary-registry.mjs --write`");
}

const expectedDlsymSymbols = new Set();
const expectedWrappers = new Set();

for (const entry of nativeExterns) {
  const key = entry.name;
  const registryEntry = nativeRegistryEntries.get(key);
  if (entry.generateBoxedWrapper) {
    if (entry.params.length === 0) {
      failures.push(`${entry.name}: compiler-generated wrapper cannot be a native constant`);
    }
    if (registryEntry !== undefined) {
      failures.push(`${entry.name}: compiler-generated wrapper must not be in the shim registry`);
    }
    continue;
  }

  expectedDlsymSymbols.add(expectedDlsymSymbol(entry));
  const wrapper = expectedWrapper(entry);
  if (wrapper) {
    expectedWrappers.add(wrapper);
  }

  if (registryEntry === undefined) {
    failures.push(`${entry.name}: missing native registry entry`);
  } else if (registryEntry.symbol !== entry.symbol) {
    failures.push(`${entry.name}: registry has ${registryEntry.symbol}, expected ${entry.symbol}`);
  }

  const dlsymSymbol = expectedDlsymSymbol(entry);
  if (registryEntry !== undefined && registryEntry.dlsymSymbol !== dlsymSymbol) {
    failures.push(`${entry.name}: registry dlsym symbol has ${registryEntry.dlsymSymbol}, expected ${dlsymSymbol}`);
  }

  if (wrapper && !boxedWrappers.has(wrapper)) {
    failures.push(`${entry.name}: missing boxed wrapper ${wrapper}`);
  }
}

for (const [key, entry] of nativeRegistryEntries.entries()) {
  if (!nativeExterns.some((nativeExtern) => nativeExtern.name === key && !nativeExtern.generateBoxedWrapper)) {
    failures.push(`${key}: native registry has extra entry`);
  }
  if (!expectedDlsymSymbols.has(entry.dlsymSymbol)) {
    failures.push(`${entry.dlsymSymbol}: native registry has extra dlsym entry`);
  }
}

for (const wrapper of boxedWrappers) {
  if (!expectedWrappers.has(wrapper)) {
    failures.push(`${wrapper}: runtime/native_symbols.cpp has extra boxed wrapper`);
  }
}

if (failures.length !== 0) {
  console.error("boundary registry check failed:");
  for (const failure of failures) {
    console.error(`- ${failure}`);
  }
  process.exit(1);
}

console.log(`boundary registry ok: ${nativeExterns.length} native externs`);
