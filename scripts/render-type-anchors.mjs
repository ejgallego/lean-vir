import { readFile, writeFile } from "node:fs/promises";
import { dirname, relative, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");

function usage() {
  console.error(`usage: node scripts/render-type-anchors.mjs --report FILE [options]

Render a Verso/Blueprint-friendly Markdown fragment for type anchors.

Options:
  --report FILE  Comparison report from check-type-anchors.
  --format FMT   Output format: markdown or html. Defaults to markdown.
  --out FILE     Write output to FILE. Defaults to stdout.
  --check        Compare rendered output with --out instead of writing it.
  -h, --help     Show this help.
`);
}

function fail(message) {
  console.error(`error: ${message}`);
  process.exit(1);
}

function parseArgs(argv) {
  let report = null;
  let out = null;
  let check = false;
  let format = "markdown";
  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index];
    switch (arg) {
      case "-h":
      case "--help":
        usage();
        process.exit(0);
      case "--report":
        report = requiredValue(argv, ++index, "--report");
        break;
      case "--format":
        format = requiredValue(argv, ++index, "--format");
        if (!["markdown", "html"].includes(format)) fail("--format must be markdown or html");
        break;
      case "--out":
        out = requiredValue(argv, ++index, "--out");
        break;
      case "--check":
        check = true;
        break;
      default:
        fail(`unknown option ${arg}`);
    }
  }
  if (report === null) fail("--report is required");
  if (check && out === null) fail("--check requires --out");
  return {
    report: resolve(root, report),
    out: out === null ? null : resolve(root, out),
    check,
    format,
  };
}

function requiredValue(argv, index, option) {
  const value = argv[index];
  if (!value || value.startsWith("--")) fail(`${option} requires a value`);
  return value;
}

const cli = parseArgs(process.argv.slice(2));
const report = JSON.parse(await readFile(cli.report, "utf8"));
const rendered = cli.format === "html" ? renderHtmlReport(report) : renderMarkdownReport(report);

if (cli.out === null) {
  process.stdout.write(rendered);
} else if (cli.check) {
  const existing = await readFile(cli.out, "utf8");
  if (existing.replace(/\r\n/g, "\n") !== rendered) {
    fail(`${relative(root, cli.out)} is stale; run npm run render:type-anchors`);
  }
  console.log(`validated ${relative(root, cli.out)}`);
} else {
  await writeFile(cli.out, rendered);
  console.log(`wrote ${relative(root, cli.out)} (${report.results.length} anchors)`);
}

function renderMarkdownReport(report) {
  const lines = [
    "<!-- Generated by scripts/render-type-anchors.mjs; edit docs/type-descriptors/*.types.d.ts or *.anchors.json instead. -->",
    "",
    "# Lean VIR Type Anchors",
    "",
    "This fragment is intentionally lightweight: each entry has a Blueprint label, a Lean declaration association when available, a TypeScript source jump, and browser-native hover text.",
    "",
    "```vir-type-anchors",
    `exact\t${report.summary.exact}`,
    `compatible\t${report.summary.compatible}`,
    `weak\t${report.summary.weak}`,
    `missing\t${report.summary.missing}`,
    "```",
    "",
  ];
  for (const result of report.results) {
    lines.push(...renderAnchor(result), "");
  }
  return `${lines.join("\n").replace(/\n+$/g, "")}\n`;
}

