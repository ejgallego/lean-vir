function summaryCard(label, count) {
  return `<div class="summary-card ${escapeAttr(label)}"><strong>${count}</strong><span>${escapeHtml(label)}</span></div>`;
}

function renderHtmlAnchor(result) {
  const ts = result.tsSymbol;
  const href = ts === undefined ? "#" : sourceHref(ts.source);
  const notes = result.notes.length === 0
    ? ""
    : `<p class="notes">${escapeHtml(result.notes.join("; "))}</p>`;
  const hover = ts?.hover
    ? `<p class="hover">${escapeHtml(ts.hover)}</p>`
    : "";
  const display = ts?.display
    ? escapeHtml(ts.display)
    : escapeHtml(JSON.stringify(result.tsSymbol?.shape ?? {}, null, 2));
  const leanDisplay = escapeHtml(formatLeanDescriptor(result.leanDescriptor));
  return `      <article class="anchor" id="${escapeAttr(`type-anchor-${slug(result.id)}`)}" data-vir-type-anchor-hover="${escapeAttr(hoverText(result))}">
        <div class="anchor-head">
          <div class="name">
            <h2 class="ts-name"><a href="${escapeAttr(href)}" title="${escapeAttr(hoverText(result))}">${escapeHtml(result.ts)}</a></h2>
            <p class="lean-name">Lean: <code>${escapeHtml(result.lean)}</code></p>
          </div>
          <span class="badge ${escapeAttr(result.status)}" title="${escapeAttr(statusTitle(result))}">${escapeHtml(result.status)}</span>
        </div>
        <div class="meta">
          <span>TypeScript source: <a href="${escapeAttr(href)}">${escapeHtml(sourceLabel(ts?.source))}</a></span>
          <span>Anchor: <code>${escapeHtml(result.id)}</code></span>
        </div>
        <div class="comparison">
          <section>
            <p class="pane-title">TypeScript declaration</p>
            <pre><code>${display}</code></pre>
          </section>
          <section>
            <p class="pane-title">Lean VIR descriptor</p>
            <pre><code>${leanDisplay}</code></pre>
          </section>
        </div>
        ${notes}
        ${hover}
      </article>`;
}

function renderAnchor(result) {
  const label = `vir_type_anchor_${slug(result.id)}`;
  const lean = result.leanDescriptor?.kind === "type" || result.leanDescriptor?.kind === "export"
    ? ` (lean := "${escapeAttr(result.lean)}")`
    : "";
  const ts = result.tsSymbol;
  const hover = hoverText(result);
  const href = ts === undefined ? "#" : sourceHref(ts.source);
  const statusTitle = result.notes.length === 0 ? result.status : `${result.status}: ${result.notes.join("; ")}`;
  return [
    `:::definition "${label}"${lean}`,
    `<div class="vir-type-anchor vir-type-anchor-${escapeAttr(result.status)}" id="${escapeAttr(label)}" data-vir-type-anchor-hover="${escapeAttr(hover)}">`,
    `<p><strong>${escapeHtml(result.lean)}</strong> <span class="vir-type-anchor-status" title="${escapeAttr(statusTitle)}">${escapeHtml(result.status)}</span></p>`,
    `<p>TypeScript: <a href="${escapeAttr(href)}" title="${escapeAttr(hover)}"><code>${escapeHtml(result.ts)}</code></a></p>`,
    ts?.display ? `<pre><code>${escapeHtml(ts.display)}</code></pre>` : "",
    result.notes.length === 0 ? "" : `<p>${escapeHtml(result.notes.join("; "))}</p>`,
    "</div>",
    ":::",
  ].filter((line) => line !== "");
}

function sourceHref(source) {
  if (source === undefined) return "#";
  const file = source.path.split("/").pop();
  const line = source.startLine === source.endLine
    ? `#L${source.startLine}`
    : `#L${source.startLine}-L${source.endLine}`;
  return `${file}${line}`;
}

function hoverText(result) {
  const parts = [
    `${result.status}: ${result.lean} -> ${result.ts}`,
    result.tsSymbol?.hover,
    result.notes.length === 0 ? null : result.notes.join("; "),
  ].filter(Boolean);
  return parts.join("\n\n");
}

function statusTitle(result) {
  return result.notes.length === 0 ? result.status : `${result.status}: ${result.notes.join("; ")}`;
}

function sourceLabel(source) {
  if (source === undefined) return "missing";
  return `${source.path}:${source.startLine}`;
}

function formatLeanDescriptor(descriptor) {
  if (descriptor === undefined) return "missing Lean descriptor";
  const header = `${descriptor.kind} ${descriptor.lean}`;
  return `${header}\n${formatShape(descriptor.shape, 0)}`;
}

function formatShape(shape, indent) {
  const pad = "  ".repeat(indent);
  if (shape === null || typeof shape !== "object") return `${pad}?`;
  switch (shape.kind) {
    case "primitive":
      return `${pad}${shape.name}`;
    case "resource":
      return `${pad}resource ${shape.name}`;
    case "opaque":
      return `${pad}opaque ${shape.name}`;
    case "ref":
      return `${pad}${shape.id}`;
    case "array":
      return formatUnaryShape("Array", shape.element, indent);
    case "option":
      return formatUnaryShape("Option", shape.element, indent);
    case "tuple":
      return `${pad}(${(shape.elements ?? []).map((item) => formatShape(item, 0).trim()).join(", ")})`;
    case "record":
      return formatRecordShape(shape, indent);
    case "enum":
      return `${pad}enum { ${(shape.cases ?? []).join(", ")} }`;
    case "variant":
      return formatVariantShape(shape, indent);
    case "function":
      return formatFunctionShape(shape, indent);
    default:
      return `${pad}${JSON.stringify(shape, null, 2)}`;
  }
}

function formatRecordShape(shape, indent) {
  const pad = "  ".repeat(indent);
  const fields = Object.entries(shape.fields ?? {});
  if (fields.length === 0) return `${pad}structure {}`;
  return [
    `${pad}structure ${shape.name ?? ""} {`,
    ...fields.map(([name, field]) => formatField(name, field, indent + 1)),
    `${pad}}`,
  ].join("\n");
}

function formatVariantShape(shape, indent) {
  const pad = "  ".repeat(indent);
  const ctors = Object.entries(shape.constructors ?? {});
  if (ctors.length === 0) return `${pad}inductive ${shape.name ?? ""} {}`;
  return [
    `${pad}inductive ${shape.name ?? ""} where`,
    ...ctors.map(([name, ctor]) => {
      const fields = Object.entries(ctor.fields ?? {});
      if (fields.length === 0) return `${"  ".repeat(indent + 1)}| ${name}`;
      const fieldText = fields
        .map(([fieldName, field]) => `${fieldName}: ${formatInlineShape(field)}`)
        .join(", ");
      return `${"  ".repeat(indent + 1)}| ${name} (${fieldText})`;
    }),
  ].join("\n");
}

function formatFunctionShape(shape, indent) {
  const pad = "  ".repeat(indent);
  const args = (shape.args ?? [])
    .map((arg) => `${arg.name}: ${formatInlineShape(arg.type)}`)
    .join(", ");
  const effect = shape.effect && shape.effect !== "pure" ? `${shape.effect} ` : "";
  return `${pad}(${args}) -> ${effect}${formatInlineShape(shape.result)}`;
}

function formatUnaryShape(name, child, indent) {
  const pad = "  ".repeat(indent);
  const childText = formatShape(child, indent + 1);
  if (!childText.includes("\n")) return `${pad}${name} ${childText.trim()}`;
  return `${pad}${name} (\n${childText}\n${pad})`;
}

function formatField(name, field, indent) {
  const pad = "  ".repeat(indent);
  const text = formatShape(field, indent + 1);
  if (!text.includes("\n")) return `${pad}${name}: ${text.trim()}`;
  return `${pad}${name}:\n${text}`;
}

function formatInlineShape(shape) {
  switch (shape?.kind) {
    case "record":
      return shape.name ?? "structure";
    case "variant":
      return shape.name ?? "inductive";
    case "function":
      return "Function";
    default:
      return formatShape(shape, 0).replace(/\s+/g, " ").trim();
  }
}

function slug(text) {
  return text.toLowerCase().replace(/[^a-z0-9]+/g, "_").replace(/^_+|_+$/g, "") || "anchor";
}

function escapeHtml(text) {
  return String(text)
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;");
}

function escapeAttr(text) {
  return escapeHtml(text).replaceAll("\"", "&quot;");
}
