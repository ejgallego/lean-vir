import assert from "node:assert/strict";
import { mkdir, readFile, writeFile } from "node:fs/promises";
import { fileURLToPath } from "node:url";

import * as esbuild from "esbuild";

const check = process.argv.includes("--check");
const entryPoint = new URL("../web/src/vir-infoview-widget.js", import.meta.url);
const outFile = new URL("../web/src/generated/vir-infoview-widget.js", import.meta.url);
const repoRoot = new URL("..", import.meta.url);

const infoviewReactDomClientPlugin = {
  name: "infoview-react-dom-client",
  setup(build) {
    build.onResolve({ filter: /^\.\/vir-react-dom-client\.js$/ }, (args) => {
      if (!args.importer.endsWith("/web/src/vir-react-host-bindings.js")) {
        return null;
      }
      return {
        namespace: "infoview-react-dom-client",
        path: "vir-react-dom-client",
      };
    });
    build.onLoad({ filter: /.*/, namespace: "infoview-react-dom-client" }, () => ({
      contents: 'export { createRoot } from "react-dom";\n',
      loader: "js",
    }));
  },
};

const result = await esbuild.build({
  absWorkingDir: fileURLToPath(repoRoot),
  banner: {
    js: [
      "/*",
      "Copyright (c) 2026 Lean FRO LLC. All rights reserved.",
      "Released under Apache 2.0 license as described in the file LICENSE.",
      "",
      "Generated by scripts/build-infoview-widget.mjs.",
      "External imports are provided by the Lean infoview import map.",
      "*/",
    ].join("\n"),
  },
  bundle: true,
  charset: "utf8",
  entryPoints: [fileURLToPath(entryPoint)],
  external: [
    "@leanprover/infoview",
    "react",
    "react-dom",
  ],
  format: "esm",
  legalComments: "none",
  logLevel: "silent",
  outfile: fileURLToPath(outFile),
  platform: "browser",
  plugins: [infoviewReactDomClientPlugin],
  sourcemap: false,
  target: "es2022",
  write: false,
});

assert.equal(result.outputFiles.length, 1);
const bundle = result.outputFiles[0].text;

if (check) {
  const existing = await readFile(outFile, "utf8").catch((error) => {
    if (error?.code === "ENOENT") {
      return null;
    }
    throw error;
  });
  if (existing !== bundle) {
    console.error("infoview widget bundle is stale; run `npm run build:infoview`");
    process.exit(1);
  }
} else {
  await mkdir(new URL(".", outFile), { recursive: true });
  await writeFile(outFile, bundle);
  console.log(`wrote ${fileURLToPath(outFile)}`);
}
